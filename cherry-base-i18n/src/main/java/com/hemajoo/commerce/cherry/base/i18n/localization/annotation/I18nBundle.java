package com.hemajoo.commerce.cherry.base.i18n.localization.annotation;

import java.lang.annotation.*;

/**
 * Annotation used to specify a resource bundle for the whole type.
 * @author <a href="mailto:christophe.resse@gmail.com">Christophe Resse</a>
 * @version 1.0.0
 */
@Documented
@Retention(RetentionPolicy.RUNTIME)
@Target( { ElementType.TYPE } )
public @interface I18nBundle
{
    /**
     * Resource bundle path and name (ex.: <b>'i18n/continent'</b>).
     * @return Resource bundle path and name.
     */
    String bundle() default "";
}
